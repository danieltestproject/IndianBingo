public class GameConfigData {
    private int numberRange;
    private int playerNum;
    private int ticketRows;
    private int ticketCols;
    private int numberPerRow;

    public int getNumberRange() {
        return numberRange;
    }

    public int getPlayerNum() {
        return playerNum;
    }

    public int getTicketRows() {
        return ticketRows;
    }

    public int getTicketCols() {
        return ticketCols;
    }

    public int getNumberPerRow() {
        return numberPerRow;
    }

    public void setNumberRange(int numberRange) {
        this.numberRange = numberRange;
    }

    public void setPlayerNum(int playerNum) {
        this.playerNum = playerNum;
    }

    public void setTicketRows(int ticketRows) {
        this.ticketRows = ticketRows;
    }

    public void setTicketCols(int ticketCols) {
        this.ticketCols = ticketCols;
    }

    public void setNumberPerRow(int numberPerRow) {
        this.numberPerRow = numberPerRow;
    }
}
